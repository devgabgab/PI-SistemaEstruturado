    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String f = txtNomeFuncionario.getText();
        String s = pswSenhaFuncionario.getText();
        String e = txtNomeEmpresa.getText();

        try {

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conectado = DriverManager.getConnection("jdbc:mysql://localhost:3306/goldenbenefitspi", "root", "mysql123");

            PreparedStatement st = conectado.prepareStatement("SELECT cargo FROM funcionario WHERE nome_funcionario_id = ? AND senha = ? AND fk_nome_empresa = ?;");
            st.setString(1, f);
            st.setString(2, s);
            st.setString(3, e);

            ResultSet resultado = st.executeQuery();

            if (resultado.next()) {

                String cargo = resultado.getString("cargo");
                dispose();
                new Menu(cargo).setVisible(true);
                //new Login().setVisible(false);

            } else {
                JOptionPane.showMessageDialog(null, "funcion치rio e/ou senha inv치lidos");
            }

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

